import UIKit.UIColor

extension UIColor {
    
    static func background() -> UIColor? {
        return UIColor(
            named: "background"
        )
    }
    
    static func accent() -> UIColor? {
        return UIColor(
            named: "btnBack"
        )
    }
    
    static func danger() -> UIColor? {
        return UIColor(
            named: "danger"
        )
    }
    
}
